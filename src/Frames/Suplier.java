    private void genarateSuplierId() {
        try {
            ResultSet dataFetch = DB.search("select count('id') as x from suplier");
            if (dataFetch.next()) {
                int count = Integer.parseInt(dataFetch.getString("x"));
                count++;
                lbl_supid.setText("SUP" + count);
                txt_fname.grabFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

