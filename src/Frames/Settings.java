    private String genarateSubId(String id) {
        String subId = "";
        try {
            ResultSet dataFetch = DB.search("select count('sub_id') as x from sub_item where sub_id like '" + id + "-Sub" + "%" + "'");
            if (dataFetch.next()) {
                int count = Integer.parseInt(dataFetch.getString("x"));
                count++;
                subId = id + "-Sub" + count;

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return subId;
    }


